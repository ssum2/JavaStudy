package my.day2;

public class Member {
	String userid;
	String userpw;
	String name;
	int age;
	
	public Member() {} // 파라미터 있는 생성자 만들 때 반드시 기본생성자 만들어 줘야함
	
	public Member(String userid, String userpw, String name, int age) {
		this.userid = userid;
		this.userpw = userpw;
		this.name = name;
		this.age = age;
	}// 파라미터 있는 Member 생성자의 끝
	
	
	
	
	
	
	
	
} // Member 클래스의 끝
